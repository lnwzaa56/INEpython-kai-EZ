boy = int(input("Enter boy "))
girl  = int(input("Enter girl"))
total = boy + girl
p_boy  =  (boy/total)*100
p_girl =   (girl/total)*100
print("p_boy in class",p_boy,"%")
print("p_girl in class",p_girl,"%")
print("p_boy in class",format (p_boy,".0f"))
print("p_girl in class",format (p_girl,".0f"))

  
